"""
Individual characters of string can be accessed using index
indexing ranges from 0 to len-1
"""

# in python string has two index
# positive indexing and negative indexing
# positive index starts from 0 (from left to right)
# negative index starts from -1 (right to left)
name = "Sabin"
print(name[0])  # first character
print(name[-1])  # last character
print(name[len(name)-1])  # last character
